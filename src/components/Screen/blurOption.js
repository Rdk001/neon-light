export const getTextBlur = (color) => {
    switch (color) {
        case "#FEFEFE":
            return "#FEFEFE";
        case "#fff6e4":
            return "#fff6e4";
        case "#FDE870":
            return "#FDE870";
        case "#FFF303":
            return "#FFF303";
        case "#FEC258":
            return "#fea300";
        case "#F800B5":
            return "#F800B5";
        case "#9700FD":
            return "#9700fd";
        case "#C33424":
            return "#ff3c00";
        case "#09FF3D":
            return "#09FF3D";
        case "#00E5FC":
            return "#00E5FC";
        case "#1E02FE":
            return "#1e03fe";
        default:
            return null;
    }
};