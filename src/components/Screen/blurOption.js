export const getTextBlur = (color) => {
    switch (color) {
        case "#FEFEFE":
            return "#FEFEFE";
        case "#FEF1E2":
            return "#FEF1E2";
        case "#FFF303":
            return "#FFF303";
        case "#FEC258":
            return "#FEC258";
        case "#FF909E":
            return "#FF909E";
        case "#BA67AB":
            return "#BA67AB";
        case "#C33424":
            return "#C33424";
        case "#AAD995":
            return "#AAD995";
        case "#00FF00":
            return "#00FF00";
        case "#7C9BC9":
            return "#7C9BC9";
        default:
            return null;
    }
};